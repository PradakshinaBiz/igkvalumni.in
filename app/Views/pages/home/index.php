        <p style="text-align: justify;">
            The Agricultural higher education was initiated in Chhattisgarh with the establishment of
            College of Agriculture, Raipur in the year 1961 by the then Government of Madhya Pradesh.
            College of Agriculture, Raipur is situated at Jora village on national highway No.6, about 9 km from
            downtown. The campus provides congenial environment for studies with lush greenery all around.
            The college is facilitated with campus houses, library, canteen, auditorium, hostels, dispensary
            building and play grounds etc. The campus is being further developed to accommodate
            cooperative stores, two more hostels and staff housing. The college is provided with sports
            facilities along a multi gym.
        </p>
        <p style="text-align: justify;">Agriculture College, Raipur follows a holistic approach for the development of the
            agriculture by integrating teaching, research and extension activities and assumes specific
            responsibility for improvement of rural life in totality. The diversity of the activities of the college is
            carried out through different departments and the departments are the pivots for the main
            functioning of the teaching, research and extension education in the university.
        </p>
        <p style="text-align: justify;">There are 17 departments altogether in the college offering UG, PG and Ph.D. programmes
            namely Agronomy, Genetics &amp; Plant Breeding, Soil Science &amp; Agricultural Chemistry,
            Entomology, Agricultural Economics, Plant Pathology, Floriculture &amp; Landscape Architecture,
            Vegetable Science, Fruit Science, Agricultural Extension, Plant Molecular Biology &amp;
            Biotechnology, Agricultural Microbiology, Agro-meteorology, Agricultural Statistics &amp;Social
            Science (Language), Plant Physiology &amp; Agricultural Bio Chemistry, and Agri-Business
            Management and Forestry.
        </p>
        <p style="text-align: justify;">To support the functions of various departments, there are service centres like Tele-
            communication centre, Computer centre, Agricultural technology information centre, Publication
            unit, Library, College instructional farm and a Dairy unit. The college is dedicated to educate and
            train students in agriculture in order to enable them to achieve perceptible value addition to their
            technical knowledge, skills and positive attitude towards life along with enhanced employability at
            the time of passing out from the college.
        </p>
        <p style="text-align: justify;">Admissions to undergraduate programme are made though Pre-Agriculture Test (PAT)
            conducted by “Madhyamic Shiksha Mandal” of the state Government.
